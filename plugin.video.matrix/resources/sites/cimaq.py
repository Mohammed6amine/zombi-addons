def __checkForNextPage(sHtmlContent):
    sPattern = '<li><a href="(.+?)">.+?&laquo;</a></li>'
    oParser = cParser()
    aResult = oParser.parse(sHtmlContent, sPattern)
 
    if (aResult[0] == True):
        #print aResult[1][0]
        return aResult[1][0]

    return False




